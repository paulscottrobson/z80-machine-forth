; *********************************************************************************************
;									Set Graphics Mode
; *********************************************************************************************

@wordx gfx.raw.setmode
		ld 		a,e 
		call 	SYS_GFX_SetMode
		pop 	de
@end

; *********************************************************************************************
;								 Write to display (raw)
; *********************************************************************************************

@wordx  gfx.raw.write
		ex 		de,hl 			
		pop 	de
		call	SYS_GFX_Write
		pop 	de
@end

; *********************************************************************************************
;								Write to display (hex)
; *********************************************************************************************

@wordx  gfx.raw.writehex
		ex 		de,hl 			
		pop 	de
		call	SYS_GFX_WriteHex
		pop 	de
@end
